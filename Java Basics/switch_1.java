public class switch_1 {
    public static void main(String[] args) {
        int x=9;
        switch (x) {
            case 1:
                System.out.println("Hello");
                break;
            case 3:
                System.out.println("GLA");
                break;
            case 2:
                System.out.println("World");
                break;
            default:
                System.out.println("Not valid input");
        }
    }
}
